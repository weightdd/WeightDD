



float fnegs (float a)
{
  return -a;
}

double fnegd (double a)
{
  return -a;
}

float fmuls (float a, float b)
{
  return a * b;
}

double fmuld (double a, double b)
{
  return a * b;
}

double fsmuld (float a, float b)
{
  return (double)a * (double)b;
}

double fnsmuld (float a, float b)
{
  return -((double)a * (double)b);
}
