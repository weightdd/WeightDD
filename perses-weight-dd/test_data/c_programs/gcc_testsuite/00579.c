


int neq (int a, int b)
{
  return a != b;
}

int eq (int a, int b)
{
  return a == b;
}

int lt (unsigned int a, unsigned int b)
{
  return a < b;
}

int leq (unsigned int a, unsigned int b)
{
  return a <= b;
}

int geq (unsigned int a, unsigned int b)
{
  return a >= b;
}

int gt (unsigned int a, unsigned int b)
{
  return a > b;
}
