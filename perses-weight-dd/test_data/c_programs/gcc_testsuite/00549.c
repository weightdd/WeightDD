


float test_fnadds(float x, float y)
{
  return -(x + y);
}

double test_fnaddd(double x, double y)
{
  return -(x + y);
}

float test_fnmuls(float x, float y)
{
  return -(x * y);
}

double test_fnmuld(double x, double y)
{
  return -(x * y);
}

double test_fnsmuld(float x, float y)
{
  return -((double)x * (double)y);
}
